      "abstract": "Part 1Development and calibration of suitably accurate functional assays for BRCA1 RING domain and BRCT domain missense substitutions could dramatically accelerate clinical classification of rare missense substitutions observed in that gene. Leveraging data from 68,000 full sequence tests of BRCA1 and BRCA2, plus data from the limited number of already classified BRCA1 RING domain missense substitutions, we used logistic regression and related techniques to evaluate three BRCA1 RING domain assays. These were recently described high throughput yeast 2-hybrid and E3 ubiquitin ligase assays, plus a newly developed mammalian 2-hybrid assay. While there were concerns about the accuracy of the yeast 2-hybrid assay and the indirect nature of the ubiquitin ligase assay, the mammalian 2-hybrid assay had excellent correlation with existing missense substitution classifications. After calibration, this assay contributed to classification of one newly reported BRCA1 missense substitution. In principal, the mammalian 2-hybrid assay could be converted to a high-throughput format that would likely retain suitable accuracy.\n\nPart 2How does one achieve clinically applicable classification of the vast majority of all possible sequence variants in disease susceptibility genes? BRCA1 is a high-risk susceptibility gene for breast and ovarian cancer. Pathogenic protein truncating variants are scattered across the open reading frame, but all known missense substitutions that are pathogenic because of missense dysfunction are located in either the amino-terminal RING domain or the carboxy-terminal BRCT domain. Heterodimerization of the BRCA1 and BARD1 RING domains is a molecularly defined obligate activity. Hence, we tested every BRCA1 RING domain missense substitution that can be created by a single nucleotide change for heterodimerization with BARD1 in a Mammalian 2-hybrid (M2H) assay. Downstream of the M2H laboratory assay, we addressed three additional challenges: assay calibration, validation thereof, and integration of the calibrated results with other available data such as computational evidence and patient/population observational data to achieve clinically applicable classification. Overall, we found that about 20% of BRCA1 RING domain missense substitutions are pathogenic. Using a Bayesian point system for data integration and variant classification, we achieved clinical classification of about 89% of observed missense substitutions. Moreover, among missense substitutions not present in the human observational data used here, we find an additional 47 with concordant computational and functional assay evidence in favor of pathogenicity; these are particularly likely to be classified as Likely Pathogenic once human observational data become available.",
